package curriculum_B;

public class Qes5 {

	public static void main(String[] args) {
		// TODO 自動生成されたメソッド・スタブ
		for (int j = 1; j <= 9; j++) {                  // 外側ループ：右側の掛ける数（列見出し）を 1～9 で回す
		    StringBuilder line = new StringBuilder();   // 1行分の文字列を貯めるバッファ（連結のたびに新しい文字列を作らないため効率的）

		    for (int i = 1; i <= 20; i++) {             // 内側ループ：左側の掛けられる数（行内の項目）を 1～20 で回す
		        line.append(                            // 1つの「九九（×）」表記を行バッファに追加していく
		            String.format(                      // 書式を揃えて文字列化（ゼロ埋め・桁数をここで統一）
		                "%03d ＊ %03d = %03d",          // フォーマット指定：3桁・0埋め（001 等）、演算子は全角の「＊」
		                i,                               // 左項：掛けられる数（1～20）
		                j,                               // 右項：掛ける数（1～9）
		                i * j                            // 結果：掛け算の答え（最大 20×9=180 なので3桁で足りる）
		            )
		        );

		        if (i < 20) line.append(" || ");        // 行の途中（最後以外）のときだけ区切り「 || 」を付ける（末尾に余分な区切りを出さない）
		    }

		    System.out.println(line);                   // 1行分（20個つなげた文字列）をまとめて出力
		}

	}

}
